import React, { useRef, useState } from "react";
import "./SwingMeter.css";
import "./Knob.css";
import useDevice from "./useDevice";

const swingLevels = [
	{ value: 0 },
	{ value: 1.5 / 9 },
	{ value: 3 / 9 },
	{ value: 1 / 2 },
	{ value: 6 / 9 },
	{ value: 7.5 / 9 },
	{ value: 1 },
];
export default function SwingMeter({ onChange }) {
	const [swingLevel, setSwingLevel] = useState(50);
	const container = useRef(null);
	const knob = useRef(null);
	const touchInt = useRef(null);
	const [angle, setAngle] = useState(0);
	const [isMoving, setIsMoving] = useState(false);
	const { isMobile } = useDevice();

	const toDeg = (rad) => {
		return (rad * 180) / Math.PI;
	};

	const handleRotate = (tx, ty) => {
		let centerX = knob.current.offsetLeft + knob.current.offsetWidth / 2;
		let centerY = knob.current.offsetTop + knob.current.offsetHeight / 2;
		let { top, left } = knob.current.getBoundingClientRect();
		let newAngle = toDeg(
			Math.atan((ty - (top + centerY)) / (tx - (left + centerX)))
		);

		if (newAngle < 0) {
			newAngle = 180 - newAngle * -1;
		}

		newAngle = Math.min(newAngle, 165);
		newAngle = Math.max(newAngle, 15);
		const swing = (newAngle - 15) / 150;
		setSwingLevel(swing);
		onChange(undefined, swing * 100);
		setAngle(newAngle - 90);
	};

	const handleMouseDown = (e) => {
		touchTimer();
		setIsMoving(true);
	};

	const handleMouseUp = (e) => {
		setIsMoving(false);
	};

	const handleMouseMove = (e) => {
		if (isMoving) {
			const { clientX: x, clientY: y } = e;
			handleRotate(x, y);
		}
	};

	const touchTimer = (e) => {
		touchInt.current = true;
		setTimeout(() => {
			touchInt.current = false;
		}, 200);
	};

	const handleTouchMove = (e) => {
		e.preventDefault();

		const { clientX: x, clientY: y } = e.touches[0];
		handleRotate(x, y);
	};

	const handleChange = (e) => {
		setSwingLevel(e.target.value);
		onChange(e, e.target.value * 100);
	};

	return (
		<div
			onMouseMove={!isMobile ? handleMouseMove : undefined}
			onMouseUp={handleMouseUp}
			onTouchMove={isMobile ? handleTouchMove : undefined}
			ref={container}
			className="swing-meter-container"
		>
			<Background />
			<div onChange={handleChange} className="radios">
				{swingLevels.map((sl) => (
					<input name="swing" type="radio" value={sl.value} />
				))}
			</div>
			{/* <Knob container={container} rotation={swingLevel} /> */}
			<div onMouseDown={handleMouseDown} className="knob-container">
				<button
					ref={knob}
					style={{
						transform: `rotate(${isMoving ? angle : swingLevel * 160 - 80}deg)`,
					}}
					className="knob"
				>
					<div className="knob-circle" />
					<div className="knob-arrow" />
				</button>
			</div>
		</div>
	);
}

